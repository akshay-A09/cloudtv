<div class="dtls1-cont">
<p>Cloud TV - A leading Smart TV OS provider is thrilled to announce our partnership with TV9 - India's Leading news network. This collaboration marks a significant milestone for both companies as we strive to revolutionize the way Indian audiences consume news & entertainment.</p>
<p>Being at the forefront of transforming the television experience, Cloud TV is leveraging the power of the Smart TV Era by offering viewers unparalleled access to a vast array of content, such as Official OTT Apps, LIVE TV Channels, on-demand shows, movies, and more. Our OS comes with a user-friendly interface & comprises all Smart TV features which completes the Smart TV Ecosystem. Our seamless software integration across devices makes us a preferred choice for Smart Manufactures/ brands, & our intelligent UI/UX has successfully captivated millions of new Smart TV buyers in India.</p>


<p>TV9, with its extensive reach and deep understanding of the Indian news landscape, has established itself as a trusted source for unbiased and accurate reporting. The network owns and operates news channels <b class="resS1_grd"> TV9 Bharatvarsh, TV9 Telugu, TV9 Kannada, TV9 Marathi, TV9 Gujarati, TV9 Bangla,</b> and the recently launched News Nine Channel. With a strong presence in multiple regional languages, TV9 delivers news and current affairs programming to millions of households across India.</p>

<p>As part of this collaboration, Cloud TV users will gain access to TV9's Live News Channels, allowing them to stay updated on the latest happenings in India and beyond. All Cloud TV devices will have access to TV9 channels increasing TV9’s reach and enabling them to connect with a larger audience and disseminate information on more Connected TVs.</p>

<p>Commenting on the partnership, Mr. Abhijeet Rajpurohit, COO of Cloud TV, said, "We are delighted to partner with TV9, a network that shares our commitment to delivering high-quality content and viewing experience to their audiences. This collaboration will enable us to provide our users across India with a comprehensive blend of news and entertainment, catering to diverse regions”.
</p>

<p>Raktim Das, Chief Growth Officer, TV9 Network (Broadcasting & Digital) said: “India’s new-age viewer is turning to connected TVs for consuming content. No wonder, in a very short span of time CTVs have become a force to reckon with. With internet-enabled CTVs offering customisation and convenience to viewers, over 22 Mn households in India have switched to this new mode of savouring content. And this is just the beginning of a new revolution sweeping the country. With Smart TVs accounting for almost 90% of the TV units sold in the country, India is expected to see a surge in CTV. TV9 Network, having firmly established itself as the No.1 news network, has taken the lead in the rapidly evolving content consumption ecosystem. <b class="resS1_grd">We are proud to partner with Cloud TV – a leading TV Operating Systems provider in India.</b> With this partnership, all TV9 Network channels, cutting across 7 languages, are now available on over 150 Smart TV brands. Our latest offering in English – News9 streaming service – can also be watched on Cloud TV that powers over 6 Mn users.”</p>


<p>Cloud TV and TV9 are excited to embark on this journey together and are confident that their collaboration will reshape the Indian News landscape. As technology continues to evolve, this partnership is poised to set new benchmarks for the industry and redefine the way Indians engage with news content. Cloud TV's state-of-the-art technology will enhance the reach of TV9's content delivery to mass audiences, ensuring a seamless and high-quality viewing experience for users.</p>

